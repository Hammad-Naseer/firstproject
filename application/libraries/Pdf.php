		// $watermark = $this->getCanvas()->open_object();
		// $w = $this->getCanvas()->get_width();
  // 		$h = $this->getCanvas()->get_height();
  // 		$this->getCanvas()->page_text(200, $h - 340, "GSIMS", $font,110, array(0.85, 0.85, 0.85),$color, 0, -40);
  // 		$this->getCanvas()->close_object();
  //  		$this->getCanvas()->add_object($watermark, 'all');

		//____________Some other information_____________
		// $this->add_info(array(
		//'Title'=> 'Fee Defaulters',
		//'Subject'=>'subject',
		//'Author'=>'company name / department',
		//'Keywords'=>'tag1, tag2, tag3'
		//));
		$this->load_html($html);
	}
}
