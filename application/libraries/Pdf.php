require_once(dirname(__FILE__) . '/dompdf/autoload.inc.php');
use Dompdf\Dompdf;

class Pdf extends DOMPDF
{	
	/**
	 * Get an instance of CodeIgniter
	 *
	 * @access	protected
	 * @return	void
	 */
	protected function ci()
	{
		return get_instance();
	}

	/**
	 * Load a CodeIgniter view into domPDF
	 *
	 * @access	public
	 * @param	string	$view The view to load
	 * @param	array	$data The view data
	 * @return	void
	 */
	public function load_view($view, $data = array())
	{
		$html = $this->ci()->load->view($view, $data, TRUE);
		//________page layouts_________

		//$this->setPaper('A4','Landscape');
		$page_orientation = $this->getPaperOrientation();

		//________font style__________
		$font = $this->getFontMetrics()->get_font("helvetica");

		//________page number__________
		if ($page_orientation == "portrait") {
			$this->getCanvas()->page_text(530, 770, "Page: {PAGE_NUM} of {PAGE_COUNT}", $font, 8, array(0,0,0));
		}elseif ($page_orientation == "Landscape") {
			$this->getCanvas()->page_text(0, 0, "Page: {PAGE_NUM} of {PAGE_COUNT}", $font, 10, array(0,0,0));
		}

		$this->getCanvas()->page_text(50, 770, "Date: ".date("Y-m-j, g:i a")." - Generated By: ".$_SESSION['name'], $font, 8, array(0,0,0));

		//_________________water mark___________________
